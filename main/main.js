// ------------------ HTML row Creation ------------------------- //


let food_card = document.querySelector(".food_card");
let rowDiv = document.createElement("div");
rowDiv.classList.add("row");
food_card.appendChild(rowDiv);

itemRender(swiggyFoods) // fun calling with obj name

function itemRender(inpObj) {
    inpObj.forEach((e) => {

        let colDiv = document.createElement("div");
        colDiv.classList.add("col");
        rowDiv.appendChild(colDiv);

        let cardDiv = document.createElement("div");
        cardDiv.classList.add("card");
        colDiv.appendChild(cardDiv);

        let img = document.createElement("img");
        img.src = e.imageURL;
        cardDiv.appendChild(img);

        let shopName = document.createElement("h1");
        shopName.innerHTML = e.shopName;
        cardDiv.appendChild(shopName);

        let stateName = document.createElement("p");
        stateName.classList.add("stateName");
        stateName.innerHTML = e.stateName;
        cardDiv.appendChild(stateName);

        // Rating row Div flex
        let ratingDiv = document.createElement("div");
        ratingDiv.classList.add("rating-row");
        cardDiv.appendChild(ratingDiv);

        let rating = document.createElement("div");
        rating.classList.add("rating-icon");
        rating.innerHTML = `<i cla
        
        ss="bi bi-star-fill"></i>`;
        ratingDiv.appendChild(rating);


        let numRate = document.createElement("span");
        numRate.innerHTML = e.rating;
        rating.appendChild(numRate);

        let dotDiv = document.createElement("div");
        dotDiv.innerHTML = "•";
        dotDiv.classList.add("dot")
        ratingDiv.appendChild(dotDiv);

        let timing = document.createElement("div");
        timing.innerHTML = e.timing + " MIN";
        ratingDiv.appendChild(timing);

        let dotDiv1 = document.createElement("div");
        dotDiv1.innerHTML = "•";
        dotDiv1.classList.add("dot")
        ratingDiv.appendChild(dotDiv1);

        let rate = document.createElement("div");
        rate.innerHTML = "Rs :" + e.rate + " FOR TWO";
        ratingDiv.appendChild(rate);


        let quickView = document.createElement("div");
        quickView.innerHTML = "Quick View";
        quickView.classList.add("quickView-cls")
        cardDiv.appendChild(quickView);

        // Rating bg color filter

        if (e.rating >= 4) {
            rating.style.backgroundColor = "#48c479"
            rating.style.color = "#fff"
        }
        else if ((e.rating >= 3.5) && (e.rating < 4)) {
            rating.style.backgroundColor = "#FC8019"
            rating.style.color = "#fff"
        }

    });
}



