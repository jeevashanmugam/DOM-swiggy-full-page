// Default sorter // Relevence 

function defaultSorter() {

    let removeCol = document.querySelectorAll(".col");
    removeCol.forEach((e) => {
        e.remove()
    });

    itemRender(swiggyFoods) // fun calling with obj name
}


// Rating  sorter
function ratingSorter() {

    let sortRatingObj = swiggyFoods.slice().sort(function (a, b) {
        return b.rating - a.rating;
    });

    let removeCol = document.querySelectorAll(".col");
    removeCol.forEach((e) => {
        e.remove();
    });

    itemRender(sortRatingObj);
}


